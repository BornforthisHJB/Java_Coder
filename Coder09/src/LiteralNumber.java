public class LiteralNumber {
    public static void main(String[] args) {
        int a = 05;
        int b = 011;
        int c = 0xF;
        int d = 0x11;

        System.out.println(a);  // 5
        System.out.println(b);  // 9
        System.out.println(c);  // 15
        System.out.println(d);  // 17
    }
}
