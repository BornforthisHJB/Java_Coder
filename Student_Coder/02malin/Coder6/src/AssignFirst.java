public class AssignFirst {
    public static void main(String[] args) {
        int a = 10;
        a += 1;  // a = a + 1;
        System.out.println(a);
        a -= 1; // a = a - 1;
        System.out.println(a);
        a *= 1; // a = a * 1;
        System.out.println(a);
        a /= 1;  // a = a / 1;
        System.out.println(a);
        a %= 1; // a = a % 1;
        System.out.println(a);
        a ^= 1;
        System.out.println(a);
        a |= 1;
        System.out.println(a);
        a <<= 1;
        System.out.println(a);
        a >>= 1;
        System.out.println(a);
        a >>>= 1;
        System.out.println(a);


    }
}
