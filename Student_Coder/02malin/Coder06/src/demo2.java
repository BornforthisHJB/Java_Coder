public class demo2 {
    public static void main(String[] args) {
        int a = 0;
        a = a + 1;
        System.out.println(a);

        a += 1;
        System.out.println(a);

        a++; // a = a + 1
        ++a; // a = a + 1
        System.out.println(a);

        // a++
        a = 0;
        System.out.println(a);
        a = a + 1;

        // ++a
        a = 0;
        a = a + 1;
        System.out.println(a);


        int c = 0;
        c--;
        System.out.println(c);

        --c;
        System.out.println(c);




    }
}
