public class QuickSand {
    public static void main(String[] args) {
        System.out.println("Heloo QuickSand!");
    }
}
//
//
/*
*
* public class QuickSand {}
* 1. 类：class
* 2、QuickSand 是类名，要与文件一致
* 3、public class 是类修饰符，告诉 java 类名要与代码文件名一致
* 4、打括号是类的内容——类体、class body
* Java 程序就是由一个个类组成的，我们编写 Java 程序的过程，其实就是编写一个个类，并且往类里面添加我们类里面内容的过程。 */

/*main 方法（main method）语法元素
* public* static void main（String[] args){}
* 1、public static void 是方法修饰符
* 2、 main 方法最为特殊的一点，它是 Java 程序的入口——就好像游戏的开始按键
* so：public static void main（String[] args) ——main 方法的定义。告诉 Java这是程序的入口
* 「也就是程序最开始运行的地方」
* 小括号内是方法的参数（parameter）
* String[] args 是方法参数；
* 打括号里面：方法的内容，又称方法体
*
/
 */