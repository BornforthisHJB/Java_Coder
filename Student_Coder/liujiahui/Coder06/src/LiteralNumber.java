public class LiteralNumber {
    public static void main(String[] args) {
        // 以 0 开头的是八进制
        int a = 05;
        int b = 011;
        // 以 0x 开头的整数位是十六进制
        int c = 0xF;
        int d = 0x11;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

    }
}
