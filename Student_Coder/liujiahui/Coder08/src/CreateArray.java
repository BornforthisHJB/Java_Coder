public class CreateArray {
    public static void main(String[] args) {
        int[] intArray = new int[9]; // 9 元素长度
        System.out.println(intArray[2]);

        // 添加数据
        intArray[0] = 0;
        intArray[1] = 1;
        intArray[2] = 2;
        System.out.println(intArray[2]);
//        System.out.println(intArray);
    }
}

