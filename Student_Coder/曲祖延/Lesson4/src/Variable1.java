public class Variable1 {
    public static void main(String[] args) {
        int a = 1;  // 创建并赋值
        System.out.println(a);
        int b;
        b = 1;
        System.out.println(b);
        int x, y;
        x = 1;
        y = 3;
        System.out.println(x);
        System.out.println(y);
        int c = 0, l = 100;
        System.out.println(c);
        System.out.println(l);
        int k;
//        int k;
        k = 1;
        k = 3;
//        control + Alt + L
    }
}
